package aulas;

public class Calculadora {
	
	
	
	public static double adiçao(double a , double b){
		return  a + b;
	}
	
	public static double subtraçao(double a , double b){
		return a - b;
	}
	
	public static double multiplicaçao(double a , double b){
		return a * b;
	}
	
	public static double divisao(double a , double b){
		return a / b;
	}
}
